class demodecrement {
    public static void main (String[] args) {
        int a=5;
        System.out.println("PreDecrement");
        System.out.println("a\t= "+a);
        System.out.println("a\t= "+ --a);
        System.out.println("a\t= "+a);

        int b=5;
        System.out.println("\nPostDecrement");
        System.out.println("a\t= "+b);
        System.out.println("a\t= "+ b--);
        System.out.println("a\t= "+b);

        System.out.println("\nSELESAI");

    }
}